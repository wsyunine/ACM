template <typename T>
inline void read(T& x)
{
    x = 0;
    bool flg = 0;
    char c = getchar();
    for(; !isdigit(c); c = getchar())
        if(c == '-') flg = 1;
    for(; isdigit(c); c = getchar()) x = x * 10 + (c ^ 48);
    if(flg) x = -x;
}

int t;
int x, y, z, a, b, c;

signed main()
{
    read(t);
    while(t--) {
        read(x), read(y), read(z), read(a), read(b), read(c);

        cout << max(x * a, max(y * b, z * c)) << endl;
    }

    return 0;
}
